#!/usr/bin/env python3
"""Build + push ``schweke_homomer_public`` to public Cloudflare D1.

Joins Schweke et al. 2024 (PMID 38325366, figshare DOI
10.6084/m9.figshare.22309177) against our candidate universe and a
``gene_identifier_public`` lookup, then UPSERTs the intersection into
the ``schweke_homomer_public`` + ``schweke_homomer_release`` tables
in the ``surfaceome_public`` D1 database.

Inputs (all relative to repo root):

  data/external/schweke_homomer_atlas/list_models_refset.csv
    The 8,195-protein Schweke reference set. Columns: model_id (e.g.
    ``Q07108_V1_4``) — the only column we need. The ``_V1_N`` suffix
    is the AF2 model index; only one model per acc is in the refset
    (rank-1 by Schweke's dimer_proba score across the 5 AF2 runs).

  data/external/schweke_homomer_atlas/full_complex_index.tsv
    One-time index of higher-order complexes in
    ``full_complexes_bigbang.zip``. Columns: uniprot_acc, af_model_num,
    stoichiometry, pdb_filename. Regenerate with the inline script in
    PROVENANCE.md when re-syncing against an updated figshare deposit.

  data/processed/candidate_universe/candidate_universe_v2.tsv
    The ~6,521-row candidate universe (gene_symbol, uniprot_accession,
    plus M1/triage vote columns we don't need here).

  D1 query: gene_identifier_public
    Stable-ID lookup keyed by uniprot_acc. Provides hgnc_id,
    gene_symbol, ensembl_gene, ncbi_gene_id for every gene in the
    intersection.

  UniProt REST API
    Per-acc ``Transmembrane`` feature list, used to compute ``is_ecd_only``
    by comparing canonical TM ranges against the Schweke PDB's residue
    coverage. Cached locally in ``.runs/schweke_uniprot_tm_cache.json``
    so a re-run is fast.

Writes:

  data/external/schweke_homomer_atlas/schweke_d1_payload.tsv
    The pre-D1 payload, one row per protein. Useful for diffing
    against a previous build before pushing.

  D1: schweke_homomer_public  (UPSERT keyed on universe_version + uniprot_acc)
  D1: schweke_homomer_release (UPSERT for the single release row)

Usage:

  uv run python scripts/build_schweke_d1_table.py             # dry-run
  uv run python scripts/build_schweke_d1_table.py --execute   # push to D1

Reproducibility: ``--execute`` is idempotent (REPLACE INTO). The script
prints the row counts for every step so a diff against a prior run
flags any silent dataset shifts (cohort change, manifest re-rank,
UniProt re-resolve)."""

